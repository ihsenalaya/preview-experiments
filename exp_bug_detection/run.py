"""
RQ4 — LLM-directed seeding detects more bugs than static fixtures.

Volume-control conditions (hardened design):
  static     — no AI enrichment (baseline)
  llm_fixed  — AI enrichment, temperature=0 (same volume, deterministic output)
  llm_free   — AI enrichment, temperature>0 (same volume, diverse output)

This three-way design separates the "volume" effect from the "content quality"
effect: static vs llm_fixed isolates quality at equal temperature; llm_fixed vs
llm_free isolates the effect of output diversity.

Protocol:
  1. Read fault-catalog.yaml (generated by mutations/generate-mutants.sh).
  2. For each subject × mutant (up to N_MUTATIONS_MAX):
     a. Build mutated image via mutations/apply-mutant.sh.
     b. Run Preview for each of the 3 seed conditions.
     c. Record whether each suite detected the mutation (outcome=Failed).
  3. Compute detection rate per condition; apply McNemar's test (paired).

Hypothesis: detection_rate[llm_free] > detection_rate[llm_fixed] > detection_rate[static].
"""
import os
import subprocess
import sys
import time
import uuid
from datetime import datetime, timezone
from pathlib import Path

import yaml

sys.path.insert(0, str(Path(__file__).parent.parent))

from harness import config as cfg_module
from harness import preview_factory as factory
from harness.results_writer import RunWriter

EXPERIMENT = "bug_detection"
CATALOG_PATH = Path(__file__).parent / "fault-catalog.yaml"


def load_catalog(max_mutations: int) -> list[dict]:
    if not CATALOG_PATH.exists():
        raise FileNotFoundError(
            "fault-catalog.yaml not found. Run mutations/generate-mutants.sh first."
        )
    with open(CATALOG_PATH) as f:
        data = yaml.safe_load(f)
    return data.get("mutants", [])[:max_mutations]


def build_mutant_image(mutant_id: int, tag: str, cfg: dict) -> str:
    registry = cfg["app"]["image"].rsplit(":", 1)[0]
    script = Path(__file__).parent / "mutations" / "apply-mutant.sh"
    subprocess.run(
        ["bash", str(script), str(mutant_id), tag],
        check=True,
        env={**os.environ, "REGISTRY": registry},
    )
    return f"{registry}:{tag}"


def _extra_for_condition(seed_mode: str, cfg: dict) -> dict:
    """Build the extra_spec dict for the given seed condition."""
    exp_cfg = cfg["experiments"]["bug_detection"]
    if seed_mode == "static":
        return {}
    if seed_mode == "llm_fixed":
        return {"aiEnrichment": {
            "enabled": True,
            "model": cfg["versions"]["llm_model"],
            "temperature": exp_cfg.get("llm_fixed_temperature", 0.0),
        }}
    # llm_free
    return {"aiEnrichment": {
        "enabled": True,
        "model": cfg["versions"]["llm_model"],
        "temperature": exp_cfg.get("llm_free_temperature", 0.7),
    }}


def run_with_condition(
    run_id: str,
    mutant_id: int,
    image: str,
    seed_mode: str,
    cfg: dict,
    subject: dict,
    s_image: str,
    p_image: str,
) -> list[dict]:
    subject_id = subject["id"]
    use_subject = subject_id != "s1-flask-catalog"
    name = factory.unique_name("bd")
    extra = _extra_for_condition(seed_mode, cfg)
    rows = []
    try:
        pr_number = cfg["app"].get("pr_number_base", 9000) - 2000 + (hash(run_id) % 900)
        factory.create(
            name, "default", image, pr_number=pr_number,
            isolation_enabled=True, extra_spec=extra,
            subject=subject if use_subject else None,
            subject_image=s_image if use_subject else None,
            probe_image=p_image if use_subject else None,
        )
        factory.wait_until_phase(
            name, "default",
            target_phases=["Running", "Failed"],
            timeout_s=cfg["experiments"]["bug_detection"]["timeout_minutes"] * 60,
        )
        factory.wait_until_tests_done(
            name, "default",
            timeout_s=cfg["experiments"]["bug_detection"]["timeout_minutes"] * 60,
        )
        status = factory.get_status(name, "default")
        tests = status.get("tests") or {}

        for suite in ("smoke", "regression", "e2e"):
            suite_status = tests.get(suite.capitalize()) or tests.get(suite) or {}
            rows.append({
                "run_id": run_id,
                "experiment": EXPERIMENT,
                "subject_id": subject_id,
                "preview_name": name,
                "isolation_enabled": "true",
                "suite": suite,
                "test_name": f"mutant_{mutant_id}_{seed_mode}",
                "outcome": suite_status.get("phase", "unknown"),
                "db_rows_before": "",
                "db_rows_after": "",
                "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            })
    finally:
        factory.delete(name, "default")
    return rows


def main():
    cfg = cfg_module.load()
    exp_cfg = cfg["experiments"]["bug_detection"]
    mutants = load_catalog(exp_cfg["n_mutations_max"])
    conditions = exp_cfg["isolation_values"]  # [static, llm_fixed, llm_free]
    subjects = cfg_module.load_enabled_subjects(cfg)
    print(f"Loaded {len(mutants)} mutants. Conditions: {conditions}. Subjects: {[s['id'] for s in subjects]}")

    with RunWriter("test_outcomes", EXPERIMENT) as writer:
        for subject in subjects:
            sid = subject["id"]
            s_image = cfg_module.subject_image(cfg, sid)
            p_image = cfg_module.probe_image(cfg)
            print(f"\n{'='*60}\nSubject: {sid}")
            for mutant in mutants:
                mid = mutant["id"]
                tag = f"mutant-{mid}-{uuid.uuid4().hex[:6]}"
                print(f"\n==> Mutant {mid} (operator={mutant.get('operator', '?')})")

                # For S1, mutate the app image; for S2-S5, mutate the adapter
                try:
                    mutated_image = build_mutant_image(mid, tag, cfg)
                except subprocess.CalledProcessError as exc:
                    print(f"  SKIP: failed to build mutant image: {exc}")
                    continue

                for condition in conditions:
                    run_id = f"{EXPERIMENT}-{sid}-mutant{mid}-{condition}-{uuid.uuid4().hex[:6]}"
                    print(f"  condition={condition}  run_id={run_id}")
                    rows = run_with_condition(run_id, mid, mutated_image, condition, cfg,
                                             subject, s_image, p_image)
                    for row in rows:
                        writer.write(row)
                    time.sleep(5)

    print(f"\nResults: {writer.path}")


if __name__ == "__main__":
    main()

"""
RQ4 — LLM-directed seeding detects more bugs than static fixtures.

Protocol:
  1. Read fault-catalog.yaml (generated by mutations/generate-mutants.sh).
  2. For each mutant (up to N_MUTATIONS_MAX):
     a. Build mutated image via mutations/apply-mutant.sh.
     b. Run Preview with seed=static (spec.aiEnrichment.enabled=false).
     c. Run Preview with seed=llm    (spec.aiEnrichment.enabled=true).
     d. Record whether each suite detected the mutation (outcome=Failed).
  3. Compute detection rate: detected_by_{static,llm} / total_mutants.

Hypothesis: detection_rate[llm] > detection_rate[static].
Statistical test: McNemar's test (paired binary outcomes per mutant).
"""
import subprocess
import sys
import time
import uuid
from datetime import datetime, timezone
from pathlib import Path

import yaml

sys.path.insert(0, str(Path(__file__).parent.parent))

from harness import config as cfg_module
from harness import preview_factory as factory
from harness.results_writer import RunWriter

EXPERIMENT = "bug_detection"
CATALOG_PATH = Path(__file__).parent / "fault-catalog.yaml"


def load_catalog(max_mutations: int) -> list[dict]:
    if not CATALOG_PATH.exists():
        raise FileNotFoundError(
            f"fault-catalog.yaml not found. Run mutations/generate-mutants.sh first."
        )
    with open(CATALOG_PATH) as f:
        data = yaml.safe_load(f)
    mutants = data.get("mutants", [])
    return mutants[:max_mutations]


def build_mutant_image(mutant_id: int, tag: str) -> str:
    script = Path(__file__).parent / "mutations" / "apply-mutant.sh"
    subprocess.run(
        ["bash", str(script), str(mutant_id), tag],
        check=True,
    )
    return f"idp-preview:{tag}"


def run_with_seed(
    run_id: str,
    mutant_id: int,
    image: str,
    seed_mode: str,
    cfg: dict,
) -> list[dict]:
    """
    seed_mode: 'static' (aiEnrichment.enabled=false) or 'llm' (aiEnrichment.enabled=true).
    Returns list of result rows.
    """
    name = factory.unique_name("bd")
    ai_enabled = seed_mode == "llm"
    extra = {}
    if ai_enabled:
        extra["aiEnrichment"] = {"enabled": True, "model": cfg["versions"]["llm_model"]}

    rows = []
    try:
        factory.create(name, "default", image, isolation_enabled=True, extra_spec=extra)
        factory.wait_until_phase(
            name, "default",
            target_phases=["Running", "Failed"],
            timeout_s=cfg["experiments"]["bug_detection"]["timeout_minutes"] * 60,
        )
        status = factory.get_status(name, "default")
        tests = status.get("tests") or {}

        for suite in ("smoke", "regression", "e2e"):
            suite_status = tests.get(suite.capitalize()) or tests.get(suite) or {}
            outcome = suite_status.get("phase", "unknown")
            rows.append({
                "run_id": run_id,
                "experiment": EXPERIMENT,
                "preview_name": name,
                "isolation_enabled": "true",
                "suite": suite,
                "test_name": f"mutant_{mutant_id}_{seed_mode}",
                "outcome": outcome,
                "db_rows_before": "",
                "db_rows_after": "",
                "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            })
    finally:
        factory.delete(name, "default")

    return rows


def main():
    cfg = cfg_module.load()
    exp_cfg = cfg["experiments"]["bug_detection"]
    mutants = load_catalog(exp_cfg["n_mutations_max"])
    print(f"Loaded {len(mutants)} mutants from catalog.")

    with RunWriter("test_outcomes", EXPERIMENT) as writer:
        for mutant in mutants:
            mid = mutant["id"]
            tag = f"mutant-{mid}-{uuid.uuid4().hex[:6]}"
            print(f"\n==> Mutant {mid} (operator={mutant.get('operator', '?')})")

            try:
                image = build_mutant_image(mid, tag)
            except subprocess.CalledProcessError as exc:
                print(f"  SKIP: failed to build mutant image: {exc}")
                continue

            for seed_mode in exp_cfg["isolation_values"]:
                run_id = f"{EXPERIMENT}-mutant{mid}-{seed_mode}-{uuid.uuid4().hex[:6]}"
                print(f"  seed_mode={seed_mode}  run_id={run_id}")
                rows = run_with_seed(run_id, mid, image, seed_mode, cfg)
                for row in rows:
                    writer.write(row)
                time.sleep(5)

    print(f"\nResults: {writer.path}")


if __name__ == "__main__":
    main()
